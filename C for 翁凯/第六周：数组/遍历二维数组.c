#include <stdio.h>
/*

二维数组数组的下标： 
[0,0]_[0,1]_[0,2]_[0,3} 
[1,0]_[1,1]_[1,2]_[1,3} 
[2,0]_[2,1]_[2,2]_[2,3} 

初始化后：
6,6,6,6
6,6,6,6
6,6,6,6

*/ 
int main()
{
	int number[3][4];	//声明一个三行四列的二维函数 
	int i,j;	//列 行
	for(i=0;i<3;i++){
		for(j=0;j<4;j++){
			number[i][j]=6;
			printf("[%d]",number[i][j]);
		}
		printf("\n");
	}
	return 0;
}
